#ifndef __UK_FD_H__
#define __UK_FD_H__

#include <uk/fd_driver.h>

struct uk_fd;

/* This is the generic internal file descriptor descriptor. */
typedef union {
  uint32_t fdid;
  struct uk_fd *ukfd;
} uk_fd_t;

/*
 * Every file descriptor has a pointer to a set of POSIX functions which can be
 * used manipulate it.
 */
struct uk_fd {
  /* The unique ID of the file descriptor. */
  uint32_t fdid;
  /* This file descriptor's operations. */
  struct uk_fd_driver *ukfd;
  /* Private data for the file descriptor. */
  void *private;
};

#endif /* __UK_FD_H__ */